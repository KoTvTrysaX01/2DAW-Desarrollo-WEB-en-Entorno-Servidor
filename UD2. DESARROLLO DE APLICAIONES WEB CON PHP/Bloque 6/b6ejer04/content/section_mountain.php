      <h1 class="mb-4 text-center text-primary fw-bold">🏔️ MONTAÑAS</h1>
      	<p>Una montaña es una eminencia topográfica (elevación natural de terreno) superior a 700 m respecto a su base. Las montañas se agrupan, a excepción de los volcanes, en cordilleras o sierras.</p>

		<p>Las montañas cubren 53 % de Asia, 58 % de América, 25 % de Europa, 17 % de Australia y 3 % de África. En total, un 24 % de la litosfera constituye masa montañosa. Un 10 % de la población mundial habita en regiones montañosas. Todos los ríos mayores nacen en áreas montañosas y más de la mitad de la humanidad depende del agua de las montañas.</p>
